public class Circle {
    double radius;
    double PI= Math.PI;
    
   public Circle (){
   
       radius=5.0;
       
   }
   double Area(){
   return PI*radius*radius;
   }    
   double perimeter (){
       return 2*PI*2*radius;
       
   }
   }
   
   
   

